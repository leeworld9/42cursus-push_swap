#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*chr;

	chr = (char *)s;
	while (*chr != c)
	{
		if (*chr == '\0')
			return (NULL);
		chr++;
	}
	return (chr);
}
